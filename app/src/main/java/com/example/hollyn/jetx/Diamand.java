package com.example.hollyn.jetx;

import android.graphics.Bitmap;
import android.graphics.Canvas;

import java.util.List;
import java.util.Random;

/**
 * Created by hollyn on 4/3/15.
 */
public class Diamand {

    private int x;
    private int y;
    private GameView gameView;
    private Bitmap bmp;
    private static int width;
    private static int height;
    private int ySpeed;
    private Random random;
    private List<Diamand> temps;
    GameSound sound;


    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * ////////////////////////////////////////////                  CONSTRUCTOR                    ///////////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public Diamand(List<Diamand> temps, GameView gameView, int x, Bitmap bitmap, GameSound sound){
        this.gameView = gameView;
        bmp = bitmap;
        width = bitmap.getWidth();
        height = bitmap.getHeight();
        this.x = x - width;
        this.y = -height*4;
        ySpeed = 15;
        this.temps = temps;
        this.sound = sound;

    }

/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
* /////////////////////////////////////////////////                  Update                   ///////////////////////////////////////////////////
* ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

    public void update(){
        if((y + height >= Avion.getY() && y <= Avion.getY() + Avion.getHeight()
                && x + width >= Avion.getX() && x <= Avion.getX() + Avion.getWidth())){
            sound.playSound("Diamand");
            temps.remove(this);
            Joueur.gainDiamand();
        }
        else if(y < gameView.getHeight()) {
            y += ySpeed;
        }else
            temps.remove(this);
    }

    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////////                  ON DRAW                   ///////////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public void ondraw(Canvas canvas){
        update();
        canvas.drawBitmap(bmp, x, y, null);
    }

    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////////                  REMOVE                   ///////////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public void remove(){
        update();
    }


    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////                  X, Y, HEIGHT, WIDTH                 //////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public static void setX(int x){
        Roche.x = x;
    }

    public static void setY(int y){
        Roche.y = y;
    }

    public static int getHeight(){
        return height;
    }

    public static int getWidth(){
        return width;
    }

    public int getYSpeed(){
        return ySpeed;
    }

    public static void setYSpeed(int ySpeed){
        Roche.ySpeed = ySpeed;
    }

}
