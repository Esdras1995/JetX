import android.graphics.Bitmap;
import android.graphics.Canvas;
import android.widget.AdapterView;

import java.util.List;
public class Rock {

    private int x;
    private int y;
    private static int x1;
    private static int y1;
    private GameView gameView;
    private Bitmap bmp;
    private static int width;
    private static int height;
    private int ySpeed;
    private static List<Rock> temps;


/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
* ////////////////////////////////////////////                  CONSTRUCTOR                    ///////////////////////////////////////////////////
* ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

    public Rock(List<Rock> temps, GameView gameView, float x, float y, Bitmap bitmap) {
        this.gameView = gameView;
        bmp = bitmap;
        width = bitmap.getWidth();
        height = bitmap.getHeight();
        this.x = (int) x;
        this.y = (int) y;
        ySpeed = 15;
        this.temps = temps;
    }

/*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
* /////////////////////////////////////////////////                  Update                   ///////////////////////////////////////////////////
* ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

    public void update(){
        if((y + height >= Avion.getY() && y <= Avion.getY() + Avion.getHeight()
                && x + width >= Avion.getX() && x <= Avion.getX() + Avion.getWidth())){
            temps.remove(this);
            Joueur.gainCoin();
        }
        else if(y < gameView.getHeight()) {
            y += ySpeed;
        }else
            temps.remove(this);
    }

    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////////                  ON DRAW                   ///////////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public void onDraw(Canvas canvas){
        update();
        canvas.drawBitmap(bmp, x, y, null);
    }

    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////////                  REMOVE                   ///////////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public void remove(){
        temps.remove(this);
    }


    /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    * /////////////////////////////////////////////                  X, Y, HEIGHT, WIDTH                 //////////////////////////////////////////////
    * ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int x){
        Roche.x = x;
    }

    public void setY(int y){
        Roche.y = y;
    }

    public int getHeight(){
        return height;
    }

    public int getWidth(){
        return width;
    }

    public int getYSpeed(){
        return ySpeed;
    }

    public void setYSpeed(int ySpeed){
        Roche.ySpeed = ySpeed;
    }

}
